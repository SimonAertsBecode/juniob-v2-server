// Helper to get stack names only
export const STACK_NAMES = STACKS.map((s) => s.name);

// Helper to get stacks by category
export const getStacksByCategory = (category: 'language' | 'skill'): Stack[] =>
  STACKS.filter((s) => s.category === category);

// Helper to validate if a stack name exists
export const isValidStack = (name: string): boolean =>
  STACKS.some((s) => s.name.toLowerCase() === name.toLowerCase());

// Helper to get the correct casing for a stack name
export const getStackName = (name: string): string | undefined =>
  STACKS.find((s) => s.name.toLowerCase() === name.toLowerCase())?.name;
